serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { text, mode } = await req.json();

    if (!text || typeof text !== "string" || text.trim().length === 0) {
      return new Response(
        JSON.stringify({ error: "텍스트가 비어있습니다." }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const models = ["google/gemini-3-flash-preview", "google/gemini-2.5-flash"];
    let response: Response | null = null;
    let lastError = "";

    for (const model of models) {
      for (let attempt = 0; attempt < 2; attempt++) {
        try {
          response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
            method: "POST",
            headers: {
              Authorization: `Bearer ${LOVABLE_API_KEY}`,
              "Content-Type": "application/json",
            },
            body: JSON.stringify({
              model,
              messages: [
                { role: "system", content: mode === "refine" ? REFINE_PROMPT : SYSTEM_PROMPT },
                { role: "user", content: text },
              ],
            }),
          });

          if (response.ok) break;

          if (response.status === 429) {
            return new Response(
              JSON.stringify({ error: "요청이 너무 많습니다. 잠시 후 다시 시도해주세요." }),
              { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
            );
          }
          if (response.status === 402) {
            return new Response(
              JSON.stringify({ error: "크레딧이 부족합니다. Settings > Workspace > Usage에서 충전해주세요." }),
              { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
            );
          }

          const errorText = await response.text();
          lastError = `${model} ${response.status}: ${errorText}`;
          console.error("AI gateway error:", lastError);
          response = null;

          // Wait briefly before retry
          if (attempt === 0) await new Promise(r => setTimeout(r, 1000));
        } catch (fetchErr) {
          lastError = `${model} fetch error: ${fetchErr}`;
          console.error(lastError);
          response = null;
          if (attempt === 0) await new Promise(r => setTimeout(r, 1000));
        }
      }
      if (response?.ok) break;
    }

    if (!response || !response.ok) {
      return new Response(
        JSON.stringify({ error: "AI 서비스가 일시적으로 불안정합니다. 잠시 후 다시 시도해주세요." }),
        { status: 200, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      throw new Error("AI 응답이 비어있습니다.");
    }

    // Parse the JSON from the AI response
    let result;
    try {
      // Extract JSON from possible markdown code blocks
      const jsonMatch = content.match(/```(?:json)?\s*([\s\S]*?)```/) || [null, content];
      result = JSON.parse(jsonMatch[1].trim());
    } catch {
      console.error("Failed to parse AI response:", content);
      // Fallback: treat the entire response as the corrected text
      result = { corrected: content.trim(), changes: [] };
    }

    return new Response(JSON.stringify(result), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("correct-text error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "알 수 없는 오류가 발생했습니다." }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
